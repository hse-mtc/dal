/**
 * @param {string} path
 * @returns {Boolean}
 */
export function isExternal(path) {
  return /^(https?:|mailto:|tel:)/.test(path);
}

/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validUsername(str) {
  const usernameRegex = /^[a-zA-Z0-9]+$/;
  return str.trim().match(usernameRegex) != null;
}

