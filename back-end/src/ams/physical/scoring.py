def convert_kettlebell_snatch(value: float, params: dict, age: int) -> int:
    weight = params.get("weight")
    if weight is None:
        raise ValueError("Missing 'weight' parameter for kettlebell snatch conversion.")

    # Определяем весовую категорию
    if weight < 70:
        category = "light"
    elif weight <= 80:
        category = "medium"
    else:
        category = "heavy"

    table = _KETTLEBELL_SNATCH_TABLES[category]
    count = int(value)

    for threshold, score in table:
        if count >= threshold:
            return score
    return 0


